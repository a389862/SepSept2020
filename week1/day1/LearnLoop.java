package week1.day1;

public class LearnLoop {

	public static void main(String[] args) {

     //print numbers from 1 to 10
		
		
	   //initialize ; condition ; increment / Decrement
		for(int i = 1 ; i <= 10; i++ ) {
			
			if(i % 2 == 0) {
				
				System.out.println(i);
				
			}
					
		}
		
		
		
		

	}

}
